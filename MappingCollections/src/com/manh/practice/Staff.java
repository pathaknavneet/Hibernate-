package com.manh.practice;

import java.util.Set;

public class Staff {

	private int id;
	private String name;
	private Set<String> contact_numbers;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Set<String> getContact_numbers() {
		return contact_numbers;
	}
	public void setContact_numbers(Set<String> contact_numbers) {
		this.contact_numbers = contact_numbers;
	}
	
	
	
	
}
